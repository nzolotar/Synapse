
/// <summary>
/// Represents the settings for API URLs.
/// </summary>
public class ApiSettings
{
    /// <summary>
    /// Gets or sets the URL for the Orders API.
    /// </summary>
    public string OrdersApiUrl { get; set; } = string.Empty;

    /// <summary>
    /// Gets or sets the URL for the Update API.
    /// </summary>
    public string UpdateApiUrl { get; set; } = string.Empty;

    /// <summary>
    /// Gets or sets the URL for the Alert API.
    /// </summary>
    public string AlertApiUrl { get; set; } = string.Empty;
}
